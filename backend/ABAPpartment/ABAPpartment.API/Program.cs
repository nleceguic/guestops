using ABAPpartment.API.Extensions;
using ABAPpartment.API.Middleware;
using ABAPpartment.Infrastructure.Persistence;
using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// ?? Base de datos ??????????????????????????????????????????????????????????
builder.Services.AddDbContext<AppDbContext>(options =>
    options.UseSqlServer(
        builder.Configuration.GetConnectionString("DefaultConnection")));

// ?? Servicios de la aplicación (DI) ???????????????????????????????????????
builder.Services.AddApplicationServices();

// ?? Autenticación JWT ??????????????????????????????????????????????????????
builder.Services.AddJwtAuthentication(builder.Configuration);

// ?? Controllers + Swagger con JWT ?????????????????????????????????????????
builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerWithJwt();

// ?? CORS para Angular en desarrollo ???????????????????????????????????????
builder.Services.AddCors(options =>
{
    options.AddPolicy("DevPolicy", policy =>
        policy.WithOrigins("http://localhost:4200")
              .AllowAnyHeader()
              .AllowAnyMethod());
});

// ???????????????????????????????????????????????????????????????????????????
var app = builder.Build();
// ???????????????????????????????????????????????????????????????????????????

// ?? Middleware de excepciones globales (debe ir primero) ??????????????????
app.UseMiddleware<GlobalExceptionMiddleware>();

if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();
app.UseCors("DevPolicy");
app.UseAuthentication();   // ? Antes de Authorization
app.UseAuthorization();
app.MapControllers();

/*if (app.Environment.IsDevelopment())
{
    app.UseDeveloperExceptionPage();  // muestra errores detallados
}*/

app.Run();